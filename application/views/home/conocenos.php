  <section  class="col-xs-12 col-sm-8 col-md-7">
    <p>
      La historia de la familia Bornay. Tiene su origen en eñ año 1897 en Sanlucar De Barrameda
      donde el ibense Carlos Bornay Verdú inicio la actividad con el nombre de 
      "Nevería Valenciana".
      Sobre 1920 Toni Bornay se queda encargado de la actividad y la Nevería 
      Valenciana pasa a llamarse HELADOS TONI.
      El negocio no se limitaba a una tienda céntrica, sino que también había 
      carritos de helados y otros puntos de ventas repartidos por la ciudad.
           </p>
      <p>
      En los años 50, la hija mayor de Toni y Elisa, Fina Bornay, 
      pasa a regentar el establecimiento de la Plaza del Cabildo y es cuando,
      Jose Bornay conocido como "Pepe El Valenciano" decide establecerse en Chipiona.       
      Montando la hoy conocida "Heladería El Valenciano" regentado por la cuarta 
      y quinta generación de heladeros artesanos.

    </p>
    <p>
      Hasta día de hoy hemos ido perfeccionando nuestros helados y servicios 
      sin perder la esencia original de los heladeros artesanos 
      de los que proviene nuestra familia.
    </p>
  </section>
